function setLang(lang) {
  if (!LANG_DATA[lang]) return;
  localStorage.setItem('dantojitos_lang', lang);
  document.documentElement.setAttribute('lang', lang);

  if (typeof _swPost === 'function' && typeof _swSyncBusy !== 'undefined' && !_swSyncBusy) {
    _swPost({ type: 'CACHE_LANG', lang });
  }

  const dict = LANG_DATA[lang];

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (dict[key] !== undefined) el.textContent = dict[key];
  });

  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    if (dict[key] !== undefined) el.placeholder = dict[key];
  });

  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.dataset.i18nTitle;
    if (dict[key] !== undefined) el.title = dict[key];
  });

  document.querySelectorAll('[data-i18n-value]').forEach(el => {
    const key = el.dataset.i18nValue;
    if (dict[key] !== undefined) el.value = dict[key];
  });

  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const key = el.dataset.i18nAria;
    if (dict[key] !== undefined) el.setAttribute('aria-label', dict[key]);
  });

  const badge = document.getElementById('langBadge');
  if (badge) badge.textContent = lang === 'es' ? 'EN' : 'ES';

  const themeBtn = document.getElementById('themeToggleBtn');
  if (themeBtn) {
    const currentTheme = document.documentElement.getAttribute('data-theme') || 'light';
    const themeLabel = themeBtn.querySelector('[data-i18n="nav.theme"]');
    if (themeLabel) {
      themeLabel.textContent = currentTheme === 'dark'
        ? (lang === 'en' ? 'Light Mode' : 'Modo Claro')
        : (lang === 'en' ? 'Dark Mode'  : 'Modo Oscuro');
    }
  }

  document.dispatchEvent(new CustomEvent('langChanged', { detail: { lang } }));
}

function getLang() {
  return localStorage.getItem('dantojitos_lang') || 'es';
}

function t(key) {
  const lang = getLang();
  return (LANG_DATA[lang] && LANG_DATA[lang][key]) || key;
}

